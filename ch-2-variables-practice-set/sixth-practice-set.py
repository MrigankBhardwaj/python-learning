# Write a Python program to calculate the square of a number entered by user

user_input = int(input("Enter the number: "))
# To find the square root of any number there are two methods 
#  First Method - number ** 2 
# Second Method = number * number
# NOTE - a ^ 2 - is not the method to find the sqaure root in the Python
square_root = user_input ** 2  # To find square you have to multiply that number by itself
print(square_root)